public class Caneta extends ProdutoPapelaria {

    private String corTinta;
    private int tipoCaneta;
    private int tamPonta;

    public Caneta() {

    }

    public String getCorTinta() {
        return corTinta;
    }

    public void setCorTinta(String corTinta) {
        this.corTinta = corTinta;
    }

    public int getTipoCaneta() {
        return tipoCaneta;
    }

    public void setTipoCaneta(int tipoCaneta) {
        this.tipoCaneta = tipoCaneta;
    }

    public int getTamPonta() {
        return tamPonta;
    }

    public void setTamPonta(int tamPonta) {
        this.tamPonta = tamPonta;
    }
    
}
